		<h2 class="comic-title">The Dark Knight Returns</h2>
		<p>Gotham City is overrun by gangs. It's become so bad that an aged Bruce Wayne puts on the bat suit one more time. What sets this story apart is the way you can feel Bruce Wayne's strength of character and iron determination, especially as an out-of-retirement-hero.</p>
		
